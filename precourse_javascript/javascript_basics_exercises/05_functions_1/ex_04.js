// Write a function that accepts a single argument, a number, and returns the result of multiplying its argument by an exponent of 2(also known as squaring the number).

// Inside the squareNumber function we return the result of the expotential operator (**) which was used to multiply our parameter num by two.

function squaredNumber(num) {
  return num**2;
}

squaredNumber(3); // 9
