// Take the string 'confetti floating everywhere' and transform it to upper case.

// First we initialize the variable string and assign to the string value.  We then call the toUpperCase method upon the variable string which returns a new string with all the characters capitalized.  The original value of string has not been mutated.

let string = 'confetti floating everywhere';

string.toUpperCase(); // "CONFETTI FLOATING EVERYWHERE"

console.log(string); // "confetti floating everywhere"
