// Write a for loop that iterates over all numbers from 1 to 100, and outputs the result of multiplying each element by 2.

/* we first set a for and initalize the variable i to the value of 0.  We then set the condition of the for loop to run as long as the value of i is less than or equal to the value of 100. We then increment the value of i by 1.  In the body of the for loop we log to the consol each value of i multiplied by 2.  After logging the value of 200 to the console, the condition proves false and we exit thew for loop.
*/

for (let i = 1; i <= 100; i += 1) {
  console.log(i * 2);
}
